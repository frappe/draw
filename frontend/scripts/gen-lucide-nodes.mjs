// Generates src/icons/lucideNodes.js — the Lucide path data for every icon this
// app references, plus a fallback. Run: `node scripts/gen-lucide-nodes.mjs`.
//
// Why a generated subset (not the full 1745-icon pack): the whole icon-nodes.json
// is ~86 KB gzipped; the app uses ~100 icons. We scan source for quoted kebab
// tokens, resolve each through ALIAS (legacy feather names → current lucide
// names), and keep the ones that are real lucide icons. Over-inclusion of a few
// non-icon strings that happen to be valid lucide names is harmless.
//
// The feather→lucide rename map lives in src/icons/lucideAlias.js (shared with
// LucideIcon.vue) so the bundle and the runtime resolve names identically.
import { execSync } from 'node:child_process'
import { writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { fileURLToPath } from 'node:url'
import path from 'node:path'
import { LUCIDE_ALIAS as ALIAS } from '../src/icons/lucideAlias.js'

const require = createRequire(import.meta.url)
const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const nodes = require(ROOT + '/node_modules/lucide-static/icon-nodes.json')
const have = new Set(Object.keys(nodes))

// Match quoted tokens down to a single char so single-letter icons (e.g. "x")
// are captured; non-icon strings are filtered out by `have.has` below.
const raw = execSync(`grep -rhoE "['\\"][a-z][a-z0-9-]{0,30}['\\"]" ${ROOT}/src`, {
  encoding: 'utf8',
})
const tokens = new Set()
for (const m of raw.matchAll(/['"]([a-z][a-z0-9-]{0,30})['"]/g)) tokens.add(m[1])

const used = new Map()
for (const t of tokens) {
  const name = ALIAS[t] || t
  if (have.has(name)) used.set(name, nodes[name])
}
used.set('circle', nodes['circle']) // fallback

const sorted = [...used.keys()].sort()
const obj = {}
for (const k of sorted) obj[k] = used.get(k)

const banner = `// AUTO-GENERATED by scripts/gen-lucide-nodes.mjs — do not edit by hand.
// Lucide icon path data (arrays of [tag, attrs]) for the icons this app uses.
// Regenerate after adding icon names: node scripts/gen-lucide-nodes.mjs
`
writeFileSync(
  ROOT + '/src/icons/lucideNodes.js',
  banner + 'export default ' + JSON.stringify(obj) + '\n',
)
console.log('bundled', sorted.length, 'lucide icons')
